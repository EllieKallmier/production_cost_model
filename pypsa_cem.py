@app.cell
def _(dispatch_by_carrier, emit_df, mo, n, price_df):
    _total_emit = emit_df["Mt_CO2"].sum()
    _total_gen = dispatch_by_carrier[dispatch_by_carrier["energy_TWh"] > 0][
        "energy_TWh"
    ].sum()
    _re_carriers = {
        "solar",
        "wind",
        "offshore_wind",
        "hydro",
        "pumped_hydro",
        "battery",
    }
    _re_gen = dispatch_by_carrier[dispatch_by_carrier["carrier"].isin(_re_carriers)][
        "energy_TWh"
    ].sum()
    _re_share = _re_gen / _total_gen * 100 if _total_gen > 0 else 0
    _avg_price = price_df["mean_price"].mean()

    mo.vstack(
        [
            mo.md("## Results summary"),
            mo.hstack(
                [
                    mo.stat(
                        label="Total system cost",
                        value=f"${n.objective / 1e9:.2f}B",
                        caption="Optimisation objective",
                    ),
                    mo.stat(
                        label="Total CO₂ (all periods)",
                        value=f"{_total_emit:.0f} Mt",
                        caption="Coal + gas emissions",
                    ),
                    mo.stat(
                        label="RE share (all periods)",
                        value=f"{_re_share:.1f}%",
                        caption="% of energy from RE",
                    ),
                    mo.stat(
                        label="Avg marginal price",
                        value=f"${_avg_price:.0f}/MWh",
                        caption="Demand-weighted, all regions",
                    ),
                ],
                justify="start",
            ),
        ]
    )
    return


