@app.cell
def _():
    # Fix PyPSA 1.x bug: DataArray(expand_series(...)) inside
    # define_storage_unit_constraints gets dim names 'dim_0'/'dim_1' while
    # component DataArrays use 'snapshot'/'name' → xarray AlignmentError.
    #
    # Patch strategy: replace the function reference that optimize.py actually
    # calls (_pso.define_storage_unit_constraints) with a wrapper that
    # temporarily substitutes a patched DataArray constructor.
    #
    # Critical Marimo constraint: stored functions must NOT close over any
    # cell-local _-prefixed names (Marimo renames them and they go out of scope).
    # All references inside stored functions use local imports + module attributes.
    import pypsa.optimization.constraints as _psc
    import pypsa.optimization.optimize as _pso

    # Stash originals in module namespace so they survive cell cleanup
    if not hasattr(_psc, "_cem_orig_DataArray"):
        _psc._cem_orig_DataArray = _psc.DataArray
    if not hasattr(_pso, "_cem_orig_su_fn"):
        _pso._cem_orig_su_fn = _pso.define_storage_unit_constraints

    # No _-prefixed names; no closures; everything via local imports / module attrs
    def cem_patched_su(n, sns):
        import pypsa.optimization.constraints as _c
        import pypsa.optimization.optimize as _o

        orig_da = _c.DataArray

        def patched_da(*args, **kwargs):
            import pypsa.optimization.constraints as _c2

            da = _c2._cem_orig_DataArray(*args, **kwargs)
            rn = {}
            if "dim_0" in da.dims:
                rn["dim_0"] = "snapshot"
            if "dim_1" in da.dims:
                rn["dim_1"] = "name"
            return da.rename(rn) if rn else da

        _c.DataArray = patched_da
        try:
            _o._cem_orig_su_fn(n, sns)
        finally:
            _c.DataArray = orig_da

    _pso.define_storage_unit_constraints = cem_patched_su
    pypsa_su_patched = True
    return


