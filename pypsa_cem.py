@app.cell
def _(CARRIER_COLOURS, CARRIER_ORDER, go, mo, n, pd, period_sel):
    _period = int(period_sel.value)
    _all_snaps = n.snapshots[n.snapshots.get_level_values("period") == _period]

    # Split into summer (December) and winter (June) snapshots
    _snaps_summer = _all_snaps[_all_snaps.get_level_values("timestep").month == 12]
    _snaps_winter = _all_snaps[_all_snaps.get_level_values("timestep").month == 6]

    # Collect carriers present in this period
    _carriers_in_period = []
    for _g in n.generators_t.p.columns:
        _car = n.generators.loc[_g, "carrier"] if _g in n.generators.index else "USE"
        if _car != "USE" and _car not in _carriers_in_period:
            _carriers_in_period.append(_car)
    _sto_carriers = []
    for _s in n.storage_units_t.p.columns:
        _car = (
            n.storage_units.loc[_s, "carrier"]
            if _s in n.storage_units.index
            else "battery"
        )
        if _car not in _carriers_in_period:
            _carriers_in_period.append(_car)
        if _car not in _sto_carriers:
            _sto_carriers.append(_car)

    _sto_dict = {c: n.storage_units_t.p[c] for c in n.storage_units_t.p.columns}
    _gen_dict = {c: n.generators_t.p[c] for c in n.generators_t.p.columns}

    def _sum_carrier(
        col_series_dict, carrier, comp_df, snaps, clip_lower=0, clip_upper=None
    ):
        total = None
        for _nm, _ser in col_series_dict.items():
            _c = comp_df.loc[_nm, "carrier"] if _nm in comp_df.index else carrier
            if _c != carrier:
                continue
            _v = _ser.loc[snaps]
            if clip_lower is not None:
                _v = _v.clip(lower=clip_lower)
            if clip_upper is not None:
                _v = _v.clip(upper=clip_upper)
            total = _v if total is None else total + _v
        return total

    def _build_dispatch_fig(snaps, season_label):
        _x = snaps.get_level_values("timestep")
        fig = go.Figure()
        # demand line
        _total_demand = sum(
            n.loads_t.p_set.get(_r + "_load", pd.Series(0, index=snaps)).loc[snaps]
            for _r in n.buses.index
        )
        fig.add_trace(
            go.Scatter(
                x=_x,
                y=_total_demand.values,
                name="Demand",
                line=dict(color="black", width=2, dash="dot"),
                mode="lines",
                legendgroup="Demand",
            )
        )
        # positive dispatch bars
        _seen_cars = {"Demand"}
        _order2 = [c for c in CARRIER_ORDER if c in _carriers_in_period]
        for _car2 in _order2:
            _gen_total = _sum_carrier(
                _gen_dict, _car2, n.generators, snaps, clip_lower=0
            )
            _sto_total = _sum_carrier(
                _sto_dict, _car2, n.storage_units, snaps, clip_lower=0
            )
            _combined = None
            for _part in [_gen_total, _sto_total]:
                if _part is not None:
                    _combined = _part if _combined is None else _combined + _part
            if _combined is not None and _combined.sum() > 1:
                fig.add_trace(
                    go.Bar(
                        x=_x,
                        y=_combined.values,
                        name=_car2 if _car2 not in _seen_cars else None,
                        showlegend=_car2 not in _seen_cars,
                        marker_color=CARRIER_COLOURS.get(_car2, "#999"),
                        legendgroup=_car2,
                    )
                )
                _seen_cars.add(_car2)
        # charging (negative) bars
        _seen_charge = set()
        for _car2 in _sto_carriers:
            _charge_total = _sum_carrier(
                _sto_dict, _car2, n.storage_units, snaps, clip_lower=None, clip_upper=0
            )
            if _charge_total is not None and _charge_total.sum() < -1:
                _lbl = f"{_car2} (charging)"
                fig.add_trace(
                    go.Bar(
                        x=_x,
                        y=_charge_total.values,
                        name=_lbl if _lbl not in _seen_charge else None,
                        showlegend=_lbl not in _seen_charge,
                        marker_color=CARRIER_COLOURS.get(_car2, "#999"),
                        marker_pattern_shape="/",
                        legendgroup=_lbl,
                        opacity=0.6,
                    )
                )
                _seen_charge.add(_lbl)
        fig.update_layout(
            barmode="relative",
            title=f"NEM dispatch — {_period} {season_label}",
            xaxis_title="Time",
            yaxis_title="Generation / Charging (MW)",
            legend_title="Technology",
            height=420,
            margin=dict(t=50, b=40),
        )
        return fig

    _fig_summer = _build_dispatch_fig(_snaps_summer, "summer (December)")
    _fig_winter = _build_dispatch_fig(_snaps_winter, "winter (June)")
    mo.vstack([_fig_summer, _fig_winter])
    return


