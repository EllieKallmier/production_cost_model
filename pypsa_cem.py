@app.cell
def _(
    DEMAND_ACTIVE,
    EXISTING_COAL,
    EXISTING_GAS,
    EXISTING_HYDRO,
    EXISTING_SOLAR_MW,
    EXISTING_WIND_MW,
    MODEL_YEARS,
    OFFSHORE_WIND_CF_VIC,
    REGIONS,
    SOLAR_CF,
    TECH_COSTS,
    TIMESTAMPS,
    WIND_CF,
    carbon_price_sl,
    cis_re_gw_sl,
    cis_storage_gw_sl,
    co2_cap_2030_sl,
    co2_cap_2040_sl,
    co2_cap_2050_sl,
    coal_offset_sl,
    dsp_cost_sl,
    dsp_gw_sl,
    mo,
    pd,
    pypsa,
    res_min_pct_sl,
    run_btn,
    vpp_gw_sl,
    wacc_sl,
):
    mo.stop(
        not run_btn.value,
        mo.callout(
            mo.md(
                "👆 **Configure scenario above, then click *Run optimisation* to solve.**"
            ),
            kind="warn",
        ),
    )

    import time as _time

    _t0 = _time.time()

    # ── parameters ────────────────────────────────────────────────────────────
    _carbon_price = carbon_price_sl.value  # $/tCO₂
    _coal_offset = coal_offset_sl.value  # years (negative = earlier)
    _vpp_mw_each = vpp_gw_sl.value * 1000 / len(REGIONS)  # MW per region
    _dsp_mw_each = dsp_gw_sl.value * 1000 / len(REGIONS)  # MW per region
    _dsp_cost = dsp_cost_sl.value  # $/MWh
    _wacc = wacc_sl.value / 100.0  # fraction
    _PERIODS = MODEL_YEARS  # [2025, 2030, 2040, 2050]
    _N = len(TIMESTAMPS)  # 56 snapshots
    _SNAP_W_OBJ = 6.0 * (8760.0 / (_N * 6.0))  # 156.43 h  (annualise to 1 yr)

    # ── base timestamps ────────────────────────────────────────────────────────
    _base_ts = pd.to_datetime(TIMESTAMPS)

    # helper: build MultiIndex CF series (same profile for every period)
    def _cf_multi(values):
        s = pd.Series(values, index=_base_ts)
        out = pd.concat([s] * len(_PERIODS), keys=_PERIODS)
        out.index.names = ["period", "timestep"]
        return out

    # ── build network ─────────────────────────────────────────────────────────
    n = pypsa.Network()

    n.investment_periods = _PERIODS
    n.investment_period_weightings["years"] = [5, 10, 10, 10]
    n.investment_period_weightings["objective"] = [1.0, 1.0, 1.0, 1.0]

    n.set_snapshots(
        pd.MultiIndex.from_product([_PERIODS, _base_ts], names=["period", "timestep"])
    )
    n.snapshot_weightings["generators"] = 6.0
    n.snapshot_weightings["objective"] = _SNAP_W_OBJ

    # ── buses ─────────────────────────────────────────────────────────────────
    for _r in REGIONS:
        n.add("Bus", _r, carrier="AC")

    # ── loads (period-varying demand, clipped to ≥ 0 MW) ─────────────────────
    for _r in REGIONS:
        _dem = pd.concat(
            [pd.Series(DEMAND_ACTIVE[yr][_r], index=_base_ts) for yr in _PERIODS],
            keys=_PERIODS,
        ).clip(lower=0)
        _dem.index.names = ["period", "timestep"]
        n.add("Load", f"{_r}_load", bus=_r, p_set=_dem)

    # ── interconnectors (bidirectional Links, one per direction) ──────────────
    for _nm, _b0, _b1, _cap in [
        ("QNI_fwd", "QLD", "NSW", 1078),
        ("QNI_rev", "NSW", "QLD", 600),
        ("Terra_fwd", "QLD", "NSW", 210),
        ("Terra_rev", "NSW", "QLD", 107),
        ("VNI_fwd", "VIC", "NSW", 1350),
        ("VNI_rev", "NSW", "VIC", 1900),
        ("Hey_fwd", "SA", "VIC", 500),
        ("Hey_rev", "VIC", "SA", 650),
        ("Murr_fwd", "SA", "VIC", 200),
        ("Murr_rev", "VIC", "SA", 220),
        ("Bass_fwd", "TAS", "VIC", 306),
        ("Bass_rev", "VIC", "TAS", 478),
    ]:
        n.add(
            "Link",
            _nm,
            bus0=_b0,
            bus1=_b1,
            p_nom=_cap,
            p_nom_extendable=False,
            efficiency=1.0,
            marginal_cost=0.0,
            build_year=2000,
            lifetime=200,
        )

    # ── unserved energy (VoLL = $20,300/MWh) ─────────────────────────────────
    for _r in REGIONS:
        n.add(
            "Generator",
            f"USE_{_r}",
            bus=_r,
            carrier="USE",
            p_nom=50000,
            marginal_cost=20300,
            build_year=2000,
            lifetime=200,
        )

    # ── existing coal ─────────────────────────────────────────────────────────
    for _c in EXISTING_COAL:
        _ret = max(_c["build_year"] + 1, min(2080, _c["default_ret"] + _coal_offset))
        n.add(
            "Generator",
            _c["name"],
            bus=_c["bus"],
            carrier="coal",
            p_nom=_c["p_nom"],
            marginal_cost=_c["mc"] + _carbon_price * _c["co2"],
            build_year=_c["build_year"],
            lifetime=_ret - _c["build_year"],
        )

    # ── existing gas (ECAA cohorts with staggered retirements) ─────────────────
    for _g in EXISTING_GAS:
        n.add(
            "Generator",
            _g["name"],
            bus=_g["bus"],
            carrier="gas",
            p_nom=_g["p_nom"],
            marginal_cost=_g["mc"] + _carbon_price * _g["co2"],
            build_year=_g["build_year"],
            lifetime=_g["ret_year"] - _g["build_year"],
        )

    # ── existing hydro (StorageUnit with constant inflow = long-run avg energy budget)
    for _h in EXISTING_HYDRO:
        _inflow_mw = _h["p_nom"] * _h["annual_cf"]
        _mxh = round(_h["annual_cf"] * 168 * 2)  # 2-week reservoir buffer
        n.add(
            "StorageUnit",
            _h["name"],
            bus=_h["bus"],
            carrier="hydro",
            p_nom=_h["p_nom"],
            inflow=_inflow_mw,
            max_hours=_mxh,
            efficiency_dispatch=0.90,
            cyclic_state_of_charge=True,
            marginal_cost=_h["mc"],
            build_year=2000,
            lifetime=200,
        )

    # ── existing committed solar + wind: 3 vintage cohorts with staggered retirements
    _VRE_COHORTS = [
        {
            "tag": "early",
            "frac": 0.25,
            "build_year": 2012,
            "lifetime": 25,
        },  # retires 2037
        {
            "tag": "mid",
            "frac": 0.40,
            "build_year": 2017,
            "lifetime": 28,
        },  # retires 2045
        {
            "tag": "late",
            "frac": 0.35,
            "build_year": 2022,
            "lifetime": 30,
        },  # retires 2052
    ]
    for _r in REGIONS:
        for _coh in _VRE_COHORTS:
            n.add(
                "Generator",
                f"Solar_exist_{_r}_{_coh['tag']}",
                bus=_r,
                carrier="solar",
                p_nom=round(EXISTING_SOLAR_MW[_r] * _coh["frac"]),
                p_max_pu=_cf_multi(SOLAR_CF[_r]),
                marginal_cost=0.0,
                build_year=_coh["build_year"],
                lifetime=_coh["lifetime"],
            )
            n.add(
                "Generator",
                f"Wind_exist_{_r}_{_coh['tag']}",
                bus=_r,
                carrier="wind",
                p_nom=round(EXISTING_WIND_MW[_r] * _coh["frac"]),
                p_max_pu=_cf_multi(WIND_CF[_r]),
                marginal_cost=0.0,
                build_year=_coh["build_year"],
                lifetime=_coh["lifetime"],
            )

    # ── VPP (home-battery fleet, StorageUnit, non-extendable) ────────────────
    if _vpp_mw_each > 0:
        for _r in REGIONS:
            n.add(
                "StorageUnit",
                f"VPP_{_r}",
                bus=_r,
                carrier="battery",
                p_nom=_vpp_mw_each,
                max_hours=2,
                efficiency_store=0.85,
                efficiency_dispatch=0.90,
                cyclic_state_of_charge=True,
                marginal_cost=0.0,
                build_year=2025,
                lifetime=30,
            )

    # ── DSP (demand-side participation, modelled as peaking generator) ────────
    if _dsp_mw_each > 0:
        for _r in REGIONS:
            n.add(
                "Generator",
                f"DSP_{_r}",
                bus=_r,
                carrier="dsp",
                p_nom=_dsp_mw_each,
                marginal_cost=_dsp_cost,
                build_year=2025,
                lifetime=30,
            )

    # ── new-entrant generators + storage (two vintages: 2025 and 2050) ────────
    _STORAGE_TECHS = {"Battery_4h", "PH_8h"}

    for _tech, _vintages in TECH_COSTS.items():
        for _build_yr, _tc in _vintages.items():
            _oc = _tc["overnight_cost"] + _tc["fom_cost"] / _wacc  # $/MW
            _mc = _tc["marginal_cost"] + _carbon_price * _tc["co2_factor"]
            _lt = _tc["lifetime"]
            _car = _tc["carrier"]

            if _tech == "Offshore_WFX":
                _regions_for_tech = ["VIC"]
            else:
                _regions_for_tech = REGIONS

            for _r in _regions_for_tech:
                _nm = f"{_tech}_{_r}_{_build_yr}"

                if _tech in _STORAGE_TECHS:
                    n.add(
                        "StorageUnit",
                        _nm,
                        bus=_r,
                        carrier=_car,
                        p_nom_extendable=True,
                        p_nom_min=0,
                        overnight_cost=_oc,
                        discount_rate=_wacc,
                        lifetime=_lt,
                        max_hours=_tc["max_hours"],
                        efficiency_store=0.85,
                        efficiency_dispatch=0.90,
                        cyclic_state_of_charge=True,
                        marginal_cost=_mc,
                        build_year=_build_yr,
                    )

                elif _tech in {"Solar_SAT", "Offshore_WFX"}:
                    _cf_src = (
                        OFFSHORE_WIND_CF_VIC
                        if _tech == "Offshore_WFX"
                        else SOLAR_CF[_r]
                    )
                    n.add(
                        "Generator",
                        _nm,
                        bus=_r,
                        carrier=_car,
                        p_nom_extendable=True,
                        p_nom_min=0,
                        overnight_cost=_oc,
                        discount_rate=_wacc,
                        lifetime=_lt,
                        p_max_pu=_cf_multi(_cf_src),
                        marginal_cost=_mc,
                        build_year=_build_yr,
                    )

                elif _tech == "Wind_WM":
                    n.add(
                        "Generator",
                        _nm,
                        bus=_r,
                        carrier=_car,
                        p_nom_extendable=True,
                        p_nom_min=0,
                        overnight_cost=_oc,
                        discount_rate=_wacc,
                        lifetime=_lt,
                        p_max_pu=_cf_multi(WIND_CF[_r]),
                        marginal_cost=_mc,
                        build_year=_build_yr,
                    )

                else:  # OCGT, CCGT, CCGT_CCS — fully dispatchable
                    n.add(
                        "Generator",
                        _nm,
                        bus=_r,
                        carrier=_car,
                        p_nom_extendable=True,
                        p_nom_min=0,
                        overnight_cost=_oc,
                        discount_rate=_wacc,
                        lifetime=_lt,
                        marginal_cost=_mc,
                        build_year=_build_yr,
                    )

    # ── policy parameter values ───────────────────────────────────────────────
    _co2_2030 = co2_cap_2030_sl.value  # Mt CO2/yr (0 = disabled)
    _co2_2040 = co2_cap_2040_sl.value  # Mt CO2/yr (0 = disabled)
    _co2_2050 = co2_cap_2050_sl.value  # Mt CO2/yr (0 = disabled)
    _res_pct = res_min_pct_sl.value  # % renewable dispatch minimum (0 = disabled)
    _cis_re = cis_re_gw_sl.value  # GW renewable capacity floor (0 = disabled)
    _cis_stor = cis_storage_gw_sl.value  # GW storage capacity floor (0 = disabled)

    # ── sanitize: create n.carriers entries from component carrier strings ────
    n.sanitize()

    # ── CO₂ cap via GlobalConstraint (type='primary_energy') ─────────────────
    # LHS = sum_{g,t in period} co2[carrier(g)] * p[g,t] * snapshot_weight (3 h)
    # To convert Mt CO₂/yr → tCO₂ accumulated over _N snapshots:
    #   constant = cap_mt * 1e6 * (_N * 3h / 8760h)
    _co2_scale = _N * 3.0 / 8760.0
    for _carr, _intensity in {"coal": 0.90, "gas": 0.50, "gas_ccs": 0.04}.items():
        if _carr in n.carriers.index:
            n.carriers.loc[_carr, "co2_emissions"] = _intensity
    for _per, _cap_mt in {2030: _co2_2030, 2040: _co2_2040, 2050: _co2_2050}.items():
        if _cap_mt > 0:
            n.add(
                "GlobalConstraint",
                f"co2_cap_{_per}",
                type="primary_energy",
                carrier_attribute="co2_emissions",
                sense="<=",
                constant=_cap_mt * 1e6 * _co2_scale,
                investment_period=_per,
            )

    # ── RES % minimum + CIS capacity floors (linopy extra_functionality) ─────
    # Note: this function is called synchronously inside n.optimize() so all
    # _-prefixed locals from this cell are still in scope (same exec call).
    _re_carriers = {"solar", "wind", "offshore_wind", "hydro"}
    _excl_carriers = {"USE", "dsp"}

    def _policy_extra(n2, sns2):
        if _res_pct <= 0 and _cis_re <= 0 and _cis_stor <= 0:
            return
        m2 = n2.model
        for _per2 in _PERIODS:
            _psns = sns2[sns2.get_level_values("period") == _per2]
            if len(_psns) == 0:
                continue

            if _res_pct > 0:
                _re_g = [
                    g
                    for g in n2.generators.index
                    if n2.generators.loc[g, "carrier"] in _re_carriers
                ]
                _re_s = [
                    s
                    for s in n2.storage_units.index
                    if n2.storage_units.loc[s, "carrier"] in _re_carriers
                ]
                _all_g = [
                    g
                    for g in n2.generators.index
                    if n2.generators.loc[g, "carrier"] not in _excl_carriers
                ]
                _all_s = [s for s in n2.storage_units.index]
                _re_terms = []
                _tot_terms = []
                if _re_g:
                    _re_terms.append(
                        m2["Generator-p"].sel(name=_re_g, snapshot=_psns).sum()
                    )
                if _re_s:
                    _re_terms.append(
                        m2["StorageUnit-p_dispatch"]
                        .sel(name=_re_s, snapshot=_psns)
                        .sum()
                    )
                if _all_g:
                    _tot_terms.append(
                        m2["Generator-p"].sel(name=_all_g, snapshot=_psns).sum()
                    )
                if _all_s:
                    _tot_terms.append(
                        m2["StorageUnit-p_dispatch"]
                        .sel(name=_all_s, snapshot=_psns)
                        .sum()
                    )
                if _re_terms and _tot_terms:
                    _re_d = (
                        _re_terms[0]
                        if len(_re_terms) == 1
                        else _re_terms[0] + _re_terms[1]
                    )
                    _tot_d = (
                        _tot_terms[0]
                        if len(_tot_terms) == 1
                        else _tot_terms[0] + _tot_terms[1]
                    )
                    m2.add_constraints(
                        _re_d - (_res_pct / 100.0) * _tot_d,
                        ">=",
                        0.0,
                        name=f"res_min_{_per2}",
                    )

            if _cis_re > 0:
                _ext_re = [
                    g
                    for g in n2.generators.index
                    if n2.generators.loc[g, "p_nom_extendable"]
                    and n2.generators.loc[g, "carrier"] in _re_carriers
                    and n2.generators.loc[g, "build_year"] <= _per2
                    and (
                        n2.generators.loc[g, "build_year"]
                        + n2.generators.loc[g, "lifetime"]
                    )
                    > _per2
                ]
                if _ext_re:
                    m2.add_constraints(
                        m2["Generator-p_nom"].sel(name=_ext_re).sum(),
                        ">=",
                        _cis_re * 1e3,
                        name=f"cis_re_{_per2}",
                    )

            if _cis_stor > 0:
                _ext_s = [
                    g
                    for g in n2.storage_units.index
                    if n2.storage_units.loc[g, "p_nom_extendable"]
                    and n2.storage_units.loc[g, "build_year"] <= _per2
                    and (
                        n2.storage_units.loc[g, "build_year"]
                        + n2.storage_units.loc[g, "lifetime"]
                    )
                    > _per2
                ]
                if _ext_s:
                    m2.add_constraints(
                        m2["StorageUnit-p_nom"].sel(name=_ext_s).sum(),
                        ">=",
                        _cis_stor * 1e3,
                        name=f"cis_storage_{_per2}",
                    )

    # ── solve ─────────────────────────────────────────────────────────────────
    n.optimize(
        solver_name="highs",
        solver_options={"output_flag": False, "threads": 4},
        multi_investment_periods=True,
        extra_functionality=_policy_extra,
    )

    _elapsed = _time.time() - _t0
    mo.output.replace(
        mo.callout(
            mo.md(
                f"✅ **Solved** in {_elapsed:.0f} s — objective = ${n.objective:,.0f}"
            ),
            kind="success",
        )
    )
    return (n,)


@app.cell
def _(MODEL_YEARS, REGIONS, n, pd):
    _PERIODS = MODEL_YEARS

    # ── helper: is a generator/storage active in a given period? ─────────────
    def _active_in(component_df, period):
        by = component_df.get("build_year", pd.Series(2000, index=component_df.index))
        lt = component_df.get("lifetime", pd.Series(200, index=component_df.index))
        return (by <= period) & (period < by + lt)

    # ── installed capacity by carrier and period ──────────────────────────────
    CARRIER_ORDER = [
        "coal",
        "gas",
        "gas_ccs",
        "hydro",
        "solar",
        "wind",
        "offshore_wind",
        "battery",
        "pumped_hydro",
        "dsp",
        "USE",
        "battery_charge",
        "pumped_hydro_charge",
        "hydro_charge",
    ]
    CARRIER_COLOURS = {
        "coal": "#4a4a4a",
        "gas": "#f4a261",
        "gas_ccs": "#e76f51",
        "hydro": "#2196F3",
        "solar": "#FFD600",
        "wind": "#43A047",
        "offshore_wind": "#00ACC1",
        "battery": "#AB47BC",
        "pumped_hydro": "#1565C0",
        "dsp": "#FF7043",
        "USE": "#B71C1C",
        "battery_charge": "#CE93D8",
        "pumped_hydro_charge": "#5C9BD6",
        "hydro_charge": "#90CAF9",
    }

    cap_rows = []
    for _period in _PERIODS:
        # generators
        for _g in n.generators.index:
            _row = n.generators.loc[_g]
            if _row["carrier"] == "USE":
                continue
            if not _active_in(n.generators, _period).loc[_g]:
                continue
            _cap = _row["p_nom_opt"] if _row["p_nom_extendable"] else _row["p_nom"]
            cap_rows.append(
                {
                    "period": _period,
                    "name": _g,
                    "carrier": _row["carrier"],
                    "p_nom_GW": _cap / 1000,
                    "region": _row["bus"],
                }
            )
        # storage units
        for _s in n.storage_units.index:
            _row = n.storage_units.loc[_s]
            if not _active_in(n.storage_units, _period).loc[_s]:
                continue
            _cap = _row["p_nom_opt"] if _row["p_nom_extendable"] else _row["p_nom"]
            cap_rows.append(
                {
                    "period": _period,
                    "name": _s,
                    "carrier": _row["carrier"],
                    "p_nom_GW": _cap / 1000,
                    "region": _row["bus"],
                }
            )

    cap_df = pd.DataFrame(cap_rows)

    # ── capacity grouped by carrier × period ─────────────────────────────────
    cap_by_carrier = (
        cap_df.groupby(["period", "carrier"])["p_nom_GW"].sum().reset_index()
    )

    # ── dispatch by carrier × period (total energy TWh, annualised) ──────────
    _snap_w = n.snapshot_weightings["generators"].iloc[0]  # 6 h
    _obj_w = n.snapshot_weightings["objective"].iloc[0]  # ~156.4 h
    _yr_w = dict(
        zip(_PERIODS, n.investment_period_weightings["years"])
    )  # {2025:5, 2030:10, 2040:10, 2050:10}

    dispatch_rows = []
    for _period in _PERIODS:
        _snaps = n.snapshots[n.snapshots.get_level_values("period") == _period]
        for _g in n.generators_t.p.columns:
            _car = (
                n.generators.loc[_g, "carrier"] if _g in n.generators.index else "USE"
            )
            _energy_TWh = (
                n.generators_t.p.loc[_snaps, _g].sum()
                * _snap_w
                * (8760.0 / (len(_snaps) * _snap_w))  # annualise
                / 1e6
            )
            dispatch_rows.append(
                {
                    "period": _period,
                    "name": _g,
                    "carrier": _car,
                    "energy_TWh": _energy_TWh,
                }
            )
        for _s in n.storage_units_t.p.columns:
            _car = (
                n.storage_units.loc[_s, "carrier"]
                if _s in n.storage_units.index
                else "battery"
            )
            _p_series = n.storage_units_t.p.loc[_snaps, _s]
            _dispatch = _p_series.clip(lower=0)
            _energy_TWh = (
                _dispatch.sum() * _snap_w * (8760.0 / (len(_snaps) * _snap_w)) / 1e6
            )
            dispatch_rows.append(
                {
                    "period": _period,
                    "name": _s,
                    "carrier": _car,
                    "energy_TWh": _energy_TWh,
                }
            )
            _charge = _p_series.clip(upper=0)
            _charge_TWh = (
                _charge.sum() * _snap_w * (8760.0 / (len(_snaps) * _snap_w)) / 1e6
            )
            if abs(_charge_TWh) > 0:
                dispatch_rows.append(
                    {
                        "period": _period,
                        "name": _s + "_charge",
                        "carrier": _car + "_charge",
                        "energy_TWh": _charge_TWh,
                    }
                )

    dispatch_df = pd.DataFrame(dispatch_rows)
    dispatch_by_carrier = (
        dispatch_df.groupby(["period", "carrier"])["energy_TWh"].sum().reset_index()
    )

    # ── marginal prices (demand-weighted average by region, per period) ───────
    price_rows = []
    for _period in _PERIODS:
        _snaps = n.snapshots[n.snapshots.get_level_values("period") == _period]
        for _r in REGIONS:
            _bus_name = _r
            if _bus_name in n.buses_t.marginal_price.columns:
                _mp = n.buses_t.marginal_price.loc[_snaps, _bus_name]
                _lmp_mean = _mp.mean()
                _lmp_max = _mp.max()
            else:
                _lmp_mean, _lmp_max = 0.0, 0.0
            price_rows.append(
                {
                    "period": _period,
                    "region": _r,
                    "mean_price": _lmp_mean,
                    "max_price": _lmp_max,
                }
            )

    price_df = pd.DataFrame(price_rows)

    # ── CO₂ emissions per period (Mt CO₂, annualised) ────────────────────────
    _CO2 = {
        "coal": 0.90,
        "gas": 0.50,
        "gas_ccs": 0.04,
        "hydro": 0.0,
        "solar": 0.0,
        "wind": 0.0,
        "offshore_wind": 0.0,
        "battery": 0.0,
        "pumped_hydro": 0.0,
        "dsp": 0.0,
        "USE": 0.0,
    }
    emit_rows = []
    for _period in _PERIODS:
        _snaps = n.snapshots[n.snapshots.get_level_values("period") == _period]
        _ann = 8760.0 / (len(_snaps) * _snap_w)
        for _g in n.generators_t.p.columns:
            _car = (
                n.generators.loc[_g, "carrier"] if _g in n.generators.index else "USE"
            )
            _ef = _CO2.get(_car, 0.0)
            if _ef == 0.0:
                continue
            _mt = n.generators_t.p.loc[_snaps, _g].sum() * _snap_w * _ann * _ef / 1e6
            emit_rows.append({"period": _period, "carrier": _car, "Mt_CO2": _mt})

    emit_df = (
        pd.DataFrame(emit_rows)
        .groupby(["period", "carrier"])["Mt_CO2"]
        .sum()
        .reset_index()
    )

    # ── new investment capacity by carrier × period ───────────────────────────
    invest_rows = []
    for _period in _PERIODS:
        for _g in n.generators.index:
            _row = n.generators.loc[_g]
            if not _row["p_nom_extendable"]:
                continue
            if int(_row.get("build_year", 0)) != _period:
                continue
            _cap = _row["p_nom_opt"]
            if _cap > 1:
                invest_rows.append(
                    {
                        "period": _period,
                        "name": _g,
                        "carrier": _row["carrier"],
                        "p_nom_GW": _cap / 1000,
                    }
                )
        for _s in n.storage_units.index:
            _row = n.storage_units.loc[_s]
            if not _row["p_nom_extendable"]:
                continue
            if int(_row.get("build_year", 0)) != _period:
                continue
            _cap = _row["p_nom_opt"]
            if _cap > 1:
                invest_rows.append(
                    {
                        "period": _period,
                        "name": _s,
                        "carrier": _row["carrier"],
                        "p_nom_GW": _cap / 1000,
                    }
                )

    invest_df = (
        pd.DataFrame(invest_rows)
        if invest_rows
        else pd.DataFrame(columns=["period", "name", "carrier", "p_nom_GW"])
    )
    invest_by_carrier = (
        invest_df.groupby(["period", "carrier"])["p_nom_GW"].sum().reset_index()
    )
    return (
        CARRIER_COLOURS,
        CARRIER_ORDER,
        cap_by_carrier,
        dispatch_by_carrier,
        emit_df,
        invest_by_carrier,
        price_df,
    )


